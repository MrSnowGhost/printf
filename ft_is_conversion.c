#include "ft_printf.h"

int	ft_is_conversion(const char *fmt)
{
	if (*fmt == 's' || *fmt == 'c' || *fmt == 'p'
		|| *fmt == 'd' || *fmt == 'u' || *fmt == 'x'
		|| *fmt == 'X' || *fmt == '%')
		return (1);
	else
		return (0);
}
